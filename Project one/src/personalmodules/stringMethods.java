package personalmodules;

public class stringMethods {

	public static void main(String[] args) {
		
		String name = "Billy Bob Joe"; // String
		System.out.println("Name: " + name); // Methods are doing specific task.
		
		/**String Methods*/
		
		System.out.println("Uppercase: " + name.toUpperCase()); // Uppercase: BILLY BOB JOE--Converting to uppercase
		System.out.println("Lowercase: " + name.toLowerCase()  ); // Lowercase: billy bob joe--Converting to uppercase
		System.out.println("First char:" + name.charAt(0)); // First char:B... Char always starts with 0
		System.out.println("Get the Length: " + name.length());// 13. will get the length.
		System.out.println("Get the Last character: " + name.charAt(12)); // e
		System.out.println("Substring: Try to keep the last name: " + name.substring(10,13));//Joe
		
		
		
		


		
		
		
	
				
				
		
		
		
		
		
		
		

	}

}
